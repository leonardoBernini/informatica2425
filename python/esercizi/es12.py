#Dati 3 numeri stampare il più piccolo, il più grande e la loro media aritmetica
n=int(input("inserisci il primo numero:\n"))
n1=int(input("inserisci il secondo numero:\n"))
n2=int(input("inserisci il terzo numero:\n"))
print("il maggiore è",max(n1,n,n2),"il minore è",min(n1,n,n2),"la somma aritmetica è",n1+n2+n)